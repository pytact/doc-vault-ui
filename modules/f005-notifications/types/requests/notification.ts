/**
 * Notification Base Interface
 * Represents the base structure of a Notification entity
 * 
 * NOTE: Notifications are not created via API - they are generated by the scheduler.
 * This interface is defined for type consistency with R2 rules.
 */
export interface NotificationBase {
  id: string; // UUID format (RFC 4122 UUID v4)
  document_id: string; // UUID format (RFC 4122 UUID v4)
  reminder_type: "30d" | "7d" | "0d"; // Reminder type enum
  is_read: boolean; // Read/unread state
  created_at: string; // ISO 8601 format, UTC timezone (Z suffix required)
  updated_at: string; // ISO 8601 format, UTC timezone (Z suffix required)
}

